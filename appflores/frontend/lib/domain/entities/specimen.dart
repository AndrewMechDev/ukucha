class Specimen {
  final String id;
  final String name;
  final double weightKg;
  final double heightCm;
  final String geneticLine;
  final String glbUrl;

  const Specimen({
    required this.id,
    required this.name,
    required this.weightKg,
    required this.heightCm,
    required this.geneticLine,
    required this.glbUrl,
  });
}
